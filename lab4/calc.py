def add(a, b): # Создаем функцию add с параметрами a, b, которая возвращает сумму параметров
    return a + b
def sub(a, b): # Создаем функцию sub с параметрами a, b, которая возвращает разность параметров
    return a-b
def mul(a, b): # Создаем функцию mul с параметрами a, b, которая возвращает произведение параметров
    return a * b
def div(a, b): # Создаем функцию div с параметрами a, b, которая возвращает частное параметров
    return a / b